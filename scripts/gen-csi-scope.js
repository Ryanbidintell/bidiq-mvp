#!/usr/bin/env node
// Generates supabase/functions/inbound-email/csi-scope.ts from the single source
// of truth: CSI_SECTION_KEYWORDS + CSI_SHEET_LOCATION_TERMS in app.html.
//
// The Supabase edge function (Deno) can't import app.html at runtime, so its
// section-scope scoring data is GENERATED here rather than hand-copied (which is
// how the two inbound implementations drifted in the first place). Re-run this
// whenever the lexicon in app.html changes:
//
//   node scripts/gen-csi-scope.js
//   (then redeploy: supabase functions deploy inbound-email)

const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const html = fs.readFileSync(path.join(root, 'app.html'), 'utf8');

function sliceLiteral(startMarker, openChar, endMarker) {
    const start = html.indexOf(startMarker);
    if (start === -1) throw new Error(`marker not found: ${startMarker}`);
    const from = html.indexOf(openChar, start);
    const end = html.indexOf(endMarker, from);
    if (end === -1) throw new Error(`close not found for: ${startMarker}`);
    const closerChar = endMarker.trim()[0]; // '}' or ']'
    const closerPos = end + endMarker.indexOf(closerChar);
    return html.slice(from, closerPos + 1); // include exactly the closer char
}

// CSI_SECTION_KEYWORDS = { ... };  → grab the object literal text
const mapText = sliceLiteral('const CSI_SECTION_KEYWORDS =', '{', '\n        };');
const CSI_SECTION_KEYWORDS = new Function(`return ${mapText}`)();

// CSI_SHEET_LOCATION_TERMS = new Set([ ... ]);  → grab the array literal
const setText = sliceLiteral('const CSI_SHEET_LOCATION_TERMS =', '[', '\n        ]);');
const locationArr = new Function(`return ${setText}`)();
const LOCATION = new Set(locationArr.map((t) => String(t).toLowerCase()));

// Derive scope terms per section = keywords minus sheet/location phrases.
const scope = {};
let total = 0, kept = 0;
for (const [code, terms] of Object.entries(CSI_SECTION_KEYWORDS)) {
    const specific = terms.filter((t) => !LOCATION.has(String(t).toLowerCase()));
    total += terms.length;
    kept += specific.length;
    if (specific.length > 0) scope[code] = specific;
}

const header = `// AUTO-GENERATED by scripts/gen-csi-scope.js from app.html CSI_SECTION_KEYWORDS.
// DO NOT EDIT BY HAND. Regenerate after changing the lexicon in app.html:
//   node scripts/gen-csi-scope.js && supabase functions deploy inbound-email
// Section -> specific scope terms (material/product/system), location/sheet
// phrases removed. Used for section-level trade scoring on inbound email.
`;
const body = `export const CSI_SECTION_SCOPE: Record<string, string[]> = ${JSON.stringify(scope, null, 2)};\n`;

const outPath = path.join(root, 'supabase', 'functions', 'inbound-email', 'csi-scope.ts');
fs.writeFileSync(outPath, header + body, 'utf8');
console.log(`✅ wrote ${path.relative(root, outPath)}: ${Object.keys(scope).length} sections (${kept}/${total} terms kept after removing location phrases)`);
